WITH LATA AS (SELECT EXTRACT(YEAR FROM ZATRUDNIONY) AS ROK, COUNT(*) AS ILOSC FROM PRACOWNICY GROUP BY EXTRACT(YEAR FROM ZATRUDNIONY)) SELECT * FROM LATA ORDER BY ILOSC DESC;

